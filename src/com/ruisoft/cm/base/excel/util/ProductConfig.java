package com.ruisoft.cm.base.excel.util;

public class ProductConfig {
	private int codeX = 0;
	private int nameX = 0;
	private int specsX = 0;
	private int codeY = 0;
	private int nameY = 0;
	private int specsY = 0;
	public int getCodeX() {
		return codeX;
	}
	public void setCodeX(int codeX) {
		this.codeX = codeX;
	}
	public int getNameX() {
		return nameX;
	}
	public void setNameX(int nameX) {
		this.nameX = nameX;
	}
	public int getSpecsX() {
		return specsX;
	}
	public void setSpecsX(int specsX) {
		this.specsX = specsX;
	}
	public int getCodeY() {
		return codeY;
	}
	public void setCodeY(int codeY) {
		this.codeY = codeY;
	}
	public int getNameY() {
		return nameY;
	}
	public void setNameY(int nameY) {
		this.nameY = nameY;
	}
	public int getSpecsY() {
		return specsY;
	}
	public void setSpecsY(int specsY) {
		this.specsY = specsY;
	}
	
}
